numSemana = int(input("Entre com um número para exibir o dia correspondente da semana: "))

if numSemana == 1:
    print("Domingo")
elif numSemana == 2:
    print("Segunda")
elif numSemana == 3:
    print("Terça")
elif numSemana == 4:
    print("Quarta")
elif numSemana == 5:
    print("Quinta")
elif numSemana == 6:
    print("Sexta")
elif numSemana == 7:
    print("Sábado")
else:
    print("Valor inválido")